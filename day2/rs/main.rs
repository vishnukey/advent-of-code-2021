enum Command {
    forward(i32),
    up(i32),
    down(i32),
}

struct Position {
    dist: i32,
    depth: i32,
    aim: i32
}

fn update(pos: Position, cmd: &Command) -> Position {
    let Position {dist: x, depth:y, aim:aim} = pos;
    match cmd {
        Command::forward(dx)   => Position { dist: x + dx, depth: y + aim * dx, aim: aim        },
        Command::up(daim)      => Position { dist: x     , depth: y           , aim: aim - daim },
        Command::down(daim)    => Position { dist: x     , depth: y           , aim: aim + daim },
    }
}

fn main() {
    let initPos = Position { dist: 0, depth: 0, aim:0 };
    let Position { dist: finalX, depth: finalY, aim:_} = DATA.iter().fold(initPos, &update);
    println!("{}", finalX * finalY);
}

